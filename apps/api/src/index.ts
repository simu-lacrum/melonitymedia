//                     в”Ңв”Җв”Җв”Җв”Җв–јв”Җв”Җв”Җв”Җв”Җв”җ     в”Ңв”Җв”Җв”Җв”Җв–јв”Җв”Җв”Җв”Җв”Җв”җ
//                     в”Ӯ PostgreSQLв”Ӯ    в”Ӯ  Worker   в”Ӯ
//                     в”Ӯ (Prisma) в”Ӯ    в”Ӯ(Patchright)в”Ӯ
//                     в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ    в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ
//
// The API server handles HTTP requests and WebSocket connections.
// It NEVER runs browser automation directly вҖ” that's the worker's job.
// в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ

import 'dotenv/config';
import express from 'express';
import { createServer } from 'http';
import cors from 'cors';
import helmet from 'helmet';
import cookieParser from 'cookie-parser';
import { createSocketServer } from './lib/socket.js';
import { firewallMiddleware } from './middleware/firewall.js';

// Route imports
import authRoutes from './routes/auth.js';
import accountsRoutes from './routes/accounts.js';
import proxiesRoutes from './routes/proxies.js';
import workspaceRoutes from './routes/workspace.js';
import videosRoutes from './routes/videos.js';
import analyticsRoutes from './routes/analytics.js';
import adminRoutes from './routes/admin.js';

